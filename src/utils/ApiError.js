class ApiError extends Error {
  constructor(
    stack = "",
    errors = [],
    message = "Internal Server Error",
    statusCode = 500
  ) {
    super(message);
    this.statusCode = statusCode;
    this.data = null;
    this.message = message;
    this.success = false;
    this.errors = errors;
    if (stack) {
      this.stack = stack;
    } else {
      Error.captureStackTrace(this, this.constructor);
    }
  }
}

export default ApiError;
